public class RoomModel {

    private int roomId;
    private int floor;
    private String description;
    private int type;
    private int status;

    public RoomModel() {
    }

    public RoomModel(int roomId, int floor, String description, int type, int status) {
        this.roomId = roomId;
        this.floor = floor;
        this.description = description;
        this.type = type;
        this.status = status;
    }

    public int getFloor() {
        return floor;
    }

    public void setFloor(int floor) {
        this.floor = floor;
    }

    public int getRoomId() {
        return roomId;
    }

    public void setRoomId(int roomId) {
        this.roomId = roomId;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    
}
